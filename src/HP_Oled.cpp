void HP_Oled_init()
{
    Serial.println("Starting OLED");
    if(!oled.begin(SSD1306_SWITCHCAPVCC, OLED_ADDR)) {
        Serial.println(F("SSD1306 allocation failed"));
    }

    /* Show Logo */    
    vTaskDelay(200);
    oled.clearDisplay();
    oled.clearDisplay();
    oled.clearDisplay();
    oled.clearDisplay();
    oled.clearDisplay();
    oled.drawBitmap(0, 0, myHPLogo, 128, 64, WHITE);
    oled.display();
    vTaskDelay(2000);
    oled.clearDisplay();
    oled.setTextColor(WHITE);
    oled.setTextSize(3);
    oled.setCursor(1,0);
    oled.println("Hotty \r\nPlate");
    oled.setTextSize(1);
    oled.println("V0.1");
    oled.display();
    Serial.println("Showing Logo");
}